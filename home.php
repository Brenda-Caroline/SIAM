			Infraestrutura
			O Parque possui as seguintes infraestruturas e equipamentos:

			Alojamentos com capacidade de 08 Pessoas
			Residência institucional;
			Guarita para recepção de visitantes;
			Quiosque, onde pode ser realizada reuniões;
			Ponto de Apoio no Gerais Santana com Capacidade de 9 Pessoas.
 
			Horário de Funcionamento
			A visitação acontece de8h00 às 17h00, todos os dias da semana. Recomenda-se entrada até as 
			15h00 no máximo.

